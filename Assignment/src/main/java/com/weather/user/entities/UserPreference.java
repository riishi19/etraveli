package com.weather.user.entities;

public class UserPreference {
	
	private String name;

	private String address;

	private int age;

	private String email;
	
	private String mobile;
	
	private String deviceId;
	
	private String mintemparature;

	private String maxtemparature;
	
	private String locationname;
	
	private String api;
	
	private String channel;
	

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * @return the mobile
	 */
	public String getMobile() {
		return mobile;
	}

	/**
	 * @param mobile the mobile to set
	 */
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	/**
	 * @return the deviceId
	 */
	public String getDeviceId() {
		return deviceId;
	}

	/**
	 * @param deviceId the deviceId to set
	 */
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	/**
	 * @return the mintemparature
	 */
	public String getMintemparature() {
		return mintemparature;
	}

	/**
	 * @param mintemparature the mintemparature to set
	 */
	public void setMintemparature(String mintemparature) {
		this.mintemparature = mintemparature;
	}

	/**
	 * @return the maxtemparature
	 */
	public String getMaxtemparature() {
		return maxtemparature;
	}

	/**
	 * @param maxtemparature the maxtemparature to set
	 */
	public void setMaxtemparature(String maxtemparature) {
		this.maxtemparature = maxtemparature;
	}

	/**
	 * @return the locationname
	 */
	public String getLocationname() {
		return locationname;
	}

	/**
	 * @param locationname the locationname to set
	 */
	public void setLocationname(String locationname) {
		this.locationname = locationname;
	}

	/**
	 * @return the api
	 */
	public String getApi() {
		return api;
	}

	/**
	 * @param api the api to set
	 */
	public void setApi(String api) {
		this.api = api;
	}

	/**
	 * @return the channel
	 */
	public String getChannel() {
		return channel;
	}

	/**
	 * @param channel the channel to set
	 */
	public void setChannel(String channel) {
		this.channel = channel;
	}

}
